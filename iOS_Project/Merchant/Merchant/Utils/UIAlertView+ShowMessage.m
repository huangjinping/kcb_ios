#import "UIAlertView+ShowMessage.h"

@implementation UIAlertView (ShowMessage)

+ (void)alertShowMessage:(NSString *)message
{
    __autoreleasing UIAlertView *alert = [[UIAlertView alloc] initWithTitle:nil message:message delegate:nil cancelButtonTitle:@"确定" otherButtonTitles:nil];

    [alert show];
}

+ (void)alertShowTitle:(NSString *)title andMessage:(NSString *)message
{
    __autoreleasing UIAlertView *alert = [[UIAlertView alloc] initWithTitle:title message:message delegate:nil cancelButtonTitle:@"确定" otherButtonTitles:nil];

    [alert show];
}

@end
